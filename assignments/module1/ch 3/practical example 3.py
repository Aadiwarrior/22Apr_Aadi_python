# Using input() function to take the input from users

#Taking a string type value

String_value=input("Enter a string value:")

#Taking a integer type value

Integer_value=int(input("Enter a integer value:"))

#Taking a float type value

float_value=float(input("Enter a float value:"))

print(f"The string value you entered was {String_value}")
print(f"The integer value you entered was {Integer_value}")
print(f"The float value you entered was {float_value}")